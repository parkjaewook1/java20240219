package c03.lecture.p1arithemetic;

public class C01Arithemetic {
    public static void main(String[] args) {
        // 산술 연산자
        // +, -, *, / , %
        // 피연산자 : numeric(정수, 실수, 문자)


        int a = 3;
        int b = 9;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

    }
}
