package ch04.lecture.p4for;

public class C04FirExercuse {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++)
            System.out.println(i);


        for (int i = 1; i <= 3; i++) {
            System.out.println("i = " + i);
        }

        for (int i = 5; i >= 1; i--) {
            System.out.println("i =" + i);
        }

        for (int i = 4; i >= 0; i--) {
            System.out.println("i = " + i);
        }
        for (int i = 0; i <= 8; i += 2) {
            System.out.println("i = " + i);
        }
        for (int i = 2; i <= 10; i += 2) {
            System.out.println("i = " + i);
        }
    }
}

